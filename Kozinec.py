def ksi(x):
  """
  >>> ksi([1,0])
  array([1, 0, 0, 0, 1, 1, 0, 0, 1])
  """
  ksi = []
  n = len(x)
  if len (x) != 2: return print (x)
  for j in range (0,2*n):
    for i in range(0,n):
      #print(i+n*j,n*n)
      if i+n*j < n*n: ksi.append(x[i]*x[j])#, print (x[i]*x[j])
      else: ksi.append(x[j - n]) #, ksi.append(x[j - n])
  ksi.append(1) 
  #print ("ksi(x) = ", ksi)
  return np.array(ksi)

